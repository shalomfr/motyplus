// POST /api/tasks/seed - טעינת משימות מהאפיון
export async function POST() {
  try {
    const session = await auth();
    if (!session?.user) {
      return NextResponse.json({ error: "לא מורשה" }, { status: 401 });
    }

    // מחק את כל המשימות הקיימות
    await prisma.task.deleteMany();

    // צור את כל המשימות — הכל בסטטוס "בביצוע"
    const tasks = await prisma.task.createMany({
      data: SEED_TASKS.map((t, i) => ({
        title: t.title,
        description: t.description || null,
        status: "IN_PROGRESS" as const,
        priority: "MEDIUM" as const,
        category: t.category,
        order: i,
      })),
    });

    return NextResponse.json({
      success: true,
      count: tasks.count,
      message: `נטענו ${tasks.count} משימות מהאפיון`,
    });
  } catch (error) {
    console.error("Error seeding tasks:", error);
    return NextResponse.json({ error: "שגיאה בטעינת משימות" }, { status: 500 });
  }
}
